package org.example;

public class Calculadora {

    public int somar(int variavelA, int variavelB){
        return variavelA + variavelB;
    }

    public int subtrair(int variavelA, int variavelB){
        return variavelA - variavelB;
    }

    public int multiplicar(int variavelA, int variavelB){
        return variavelA * variavelB;
    }

    public int dividir(int variavelA, int variavelB){
        return variavelA / variavelB;
    }
}
